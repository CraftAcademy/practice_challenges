"The simplest form of conditionals are the if-statements (boolean expressions of true/false as conditional statements).
If something is a special way then there will be a certain action. If not, it will not, or else something else will happen.
Conditionals could be chained with one or many elseif efter if and before else to mark another branch/branches that can be executed.

Another type of conditionals are switch-statements. They first state a case and action happens with the first stated alternative that
matches the set case."
